#Sorting a list, reversing a list, slicing a list

lists = [3,1,4,1,5,9]

#Sorting a list
sorted_list = sorted(lists)

#Reversing a list
reversed_list = list(reversed(lists))

#Slicing a list
sliced_list = lists[2:4]

print("Sorted list :", sorted_list)
print("Reversed list :", reversed_list)
print("Sliced list :", sliced_list)