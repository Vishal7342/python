'''
Program to show single-line and multi-line string:
'''

# Single-line string
single_line_string = "Hello, World!"
print(single_line_string)

# Multi-line string
multi_line_string = """This is a 
multi-line string."""
print(multi_line_string)

# Output:

# Hello, World!
# This is a 
# multi-line string.
